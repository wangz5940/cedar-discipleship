import { defineStore } from 'pinia';

export const useAppStateStore = defineStore('appState', {
  state: () => ({
    booted: false,
    authenticated: false,
    user: null,
    tab: 'home',
    adminSection: 'members',
    sidebarCollapsed: true,
    pageTitle: 'Cedar Discipleship',
    navItems: [],
    groups: [],
    currentGroupID: 0,
    defaultGroupID: 0,
    showGroupPicker: false,
    toast: '',
    resources: [],
    members: [],
    canAdmin: false,
    canEditLearning: false,
    adminLoading: false,
    learningConfig: {},
    weekDraft: null,
    weeks: [],
    resourceLibrary: [],
    calendar: null,
  }),
  actions: {
    setSnapshot(snapshot) {
      Object.assign(this, {
        booted: true,
        authenticated: Boolean(snapshot?.authenticated),
        user: snapshot?.user || null,
        tab: snapshot?.tab || 'home',
        adminSection: snapshot?.adminSection || 'members',
        sidebarCollapsed: snapshot?.sidebarCollapsed !== false,
        pageTitle: snapshot?.pageTitle || 'Cedar Discipleship',
        navItems: Array.isArray(snapshot?.navItems) ? snapshot.navItems : [],
        groups: Array.isArray(snapshot?.groups) ? snapshot.groups : [],
        currentGroupID: Number(snapshot?.currentGroupID || 0),
        defaultGroupID: Number(snapshot?.defaultGroupID || 0),
        showGroupPicker: Boolean(snapshot?.showGroupPicker),
        toast: snapshot?.toast || '',
        resources: Array.isArray(snapshot?.resources) ? snapshot.resources : [],
        members: Array.isArray(snapshot?.members) ? snapshot.members : [],
        canAdmin: Boolean(snapshot?.canAdmin),
        canEditLearning: Boolean(snapshot?.canEditLearning),
        adminLoading: Boolean(snapshot?.adminLoading),
        learningConfig: snapshot?.learningConfig || {},
        weekDraft: snapshot?.weekDraft || null,
        weeks: Array.isArray(snapshot?.weeks) ? snapshot.weeks : [],
        resourceLibrary: Array.isArray(snapshot?.resourceLibrary) ? snapshot.resourceLibrary : [],
        calendar: snapshot?.calendar || null,
      });
    },
  },
});
